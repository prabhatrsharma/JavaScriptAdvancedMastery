// Topic: 3.2.2 Array

/* Concepts:
- Arrays in JavaScript are ordered collections of elements created using the Array constructor.
- They can contain elements of different types and can change in size and content dynamically.
- Elements in an array are accessed using their index, which is a numeric value representing their position in the array.
*/

// Code:
// Example demonstrating basic usage of arrays in JavaScript
let fruits = ['apple', 'banana', 'orange'];
console.log(fruits[0]); // Output: 'apple' (accessing the first element)
fruits.push('grapes'); // Add 'grapes' to the end
console.log(fruits.length); // Output: 4 (length of the array after adding 'grapes')
