// Topic: 3.1.11 String as an array of characters
/* Concepts:
- A string can be treated as an array of characters in JavaScript.
- Individual characters in a string can be accessed using square brackets and an index (position) or the charAt method.
*/

// Code:
// Example demonstrating accessing characters in a string as an array
let title = "Alien 10";
console.log(title[0]); // -> A
console.log(title.charAt(0)); // -> A