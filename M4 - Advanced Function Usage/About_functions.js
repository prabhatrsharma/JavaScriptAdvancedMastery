// 4.0.1 A bit more about functions

// Functions in JavaScript are versatile and powerful tools.
// Key concepts include:
// - Declaring functions: Using the function keyword or function expressions.
// - Arrow functions: A concise way to write functions, especially for callbacks.
// - Calling functions: Executing functions by their name followed by parentheses.
// - Passing arguments: Providing values to functions as parameters.
// - Returning results: Functions can return values using the return keyword.

// Additional capabilities of JavaScript functions:
// - Default parameter values: Assigning default values to function parameters.
// - Rest parameters: Gathering multiple function arguments into an array.
// - Spread syntax: Spreading elements of an array or object as function arguments.
// - Closures: Functions maintaining access to variables from their outer scope.
// - Asynchronous execution: Using callbacks, promises, or async/await for async tasks.

// Example of using default parameter values:
function greet(name = 'Guest') {
    console.log(`Hello, ${name}!`);
}

greet(); // Output: Hello, Guest!
greet('Prabhat'); // Output: Hello, Prabhat!

// These are fundamental concepts that form the basis for advanced JavaScript programming.